package main

import (
	"fmt"
	"strconv"
	"strings"
)

func main() {
	entries := strings.Split(Puzzle, "\n")

	for i, entry := range entries {
		entryVal, _ := strconv.Atoi(entry)
		for j := i + 1; j < len(entries); j++ {
			nextVal, _ := strconv.Atoi(entries[j])
			for k := j + 1; k < len(entries); k++ {
				thirdVal, _ := strconv.Atoi(entries[k])
				if entryVal+nextVal+thirdVal == 2020 {
					fmt.Printf("Solution (%d x %d X %d) %d\n", entryVal, nextVal, thirdVal, entryVal*nextVal*thirdVal)
				}
			}
		}
	}
}

